                    <p>A gas nuclear reactor (or gas fueled reactor or vapor core reactor) is a proposed kind of nuclear reactor in which the nuclear fuel would be in a gaseous state rather than liquid or solid. In this type of reactor, the only temperature-limiting materials would be the reactor walls. Conventional reactors have stricter limitations because the core would melt if the fuel temperature were to rise too high. It may also be possible to confine gaseous fission fuel magnetically, electrostatically or electrodynamically so that it would not touch (and melt) the reactor walls. A potential benefit of the gaseous reactor core concept is that instead of relying on the traditional Rankine or Brayton conversion cycles, it may be possible to extract electricity magnetohydrodynamically, or with simple direct electrostatic conversion of the charged particles.</p>
