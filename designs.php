                    <p>Fusion power is a proposed form of power generation that would generate electricity by using heat from nuclear fusion reactions. In a fusion process, two lighter atomic nuclei combine to form a heavier nucleus, while releasing energy. Devices designed to harness this energy are known as fusion reactors.
                        As a source of power, nuclear fusion is expected to have many advantages over fission. These include reduced radioactivity in operation and little high-level nuclear waste, ample fuel supplies, and increased safety. However, the necessary combination of temperature, pressure, and duration has proven to be difficult to produce in a practical and economical manner. Research into fusion reactors began in the 1940s, but to date, no design has produced more fusion power output than the electrical power input, defeating the purpose.[1] A second issue that affects common reactions is managing neutrons that are released during the reaction, which over time degrade many common materials used within the reaction chamber.
